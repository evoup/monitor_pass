 */
function getFunctions() {
    $ret = [];
	if (($handle = fopen("functions.csv", "r")) !== FALSE) {
		while (($data = fgetcsv($handle, 1000, ",")) !== FALSE) {
            //print_r($data);
            $ret[]=$data;
		}
		fclose($handle);
	}
    return $ret;
}

