/**
 *@brief 添加监控集合
 */
function getSets() {
    $ret = [];
	if (($handle = fopen("applications.csv", "r")) !== FALSE) {
		while (($data = fgetcsv($handle, 1000, ",")) !== FALSE) {
            $ret[]=$data;
		}
		fclose($handle);
	}
    return $ret;
}
